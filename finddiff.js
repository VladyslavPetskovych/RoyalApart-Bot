  function findDuplicates(arr) {
    const wubidMap = new Map();
    const nameMap = new Map();
    const duplicateWubids = [];
    const duplicateNames = [];

    arr.forEach(obj => {
        // Check duplicates by wubid
        if (obj.wubid !== undefined) {
            if (wubidMap.has(obj.wubid)) {
                duplicateWubids.push(obj);
            } else {
                wubidMap.set(obj.wubid, obj);
            }
        }

        // Check duplicates by name
        if (obj.name) {
            if (nameMap.has(obj.name)) {
                duplicateNames.push(obj.name);
            } else {
                nameMap.set(obj.name, obj);
            }
        }
    });

    return { duplicateWubids, duplicateNames };
}

